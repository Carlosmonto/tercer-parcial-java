class Aeropuerto {
    String codigo;
    String nombre;
    String poblacion;
    String pais;
    int gmt;

    public Aeropuerto(String codigo, String nombre, String poblacion, String pais, int gmt) {
        this.codigo = codigo;
        this.nombre = nombre;
        this.poblacion = poblacion;
        this.pais = pais;
        this.gmt = gmt;
    }
}
